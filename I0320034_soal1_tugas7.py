# Contoh program menggunakan fungsi string
# Masukkan string
string = str(input("Masukkan nama Anda : "))

# Mengubah menjadi huruf Kapital
kapital = string.capitalize()
print("Halo", kapital)

# Mengubah menjadi uppercase
upper = string.upper()
print("HALO", upper)

# Mengubah menjadi lowercase
lower = string.lower()
print("halo", lower)
